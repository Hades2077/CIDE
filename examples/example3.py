def sum_numbers(x, y):
    """Add two values."""
    total = x + y
    return total

def difference(x, y):
    """Find difference."""
    return x - y

def product(x, y):
    """Calculate product."""
    return x * y

def quotient(x, y):
    """Calculate quotient."""
    if y != 0:
        return x / y
    else:
        return None
